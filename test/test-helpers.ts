import {
  CoreImageAttributes,
  CoreSourceAttributes,
  transformProps,
  transformSourceProps,
  UnpicImageProps,
  UnpicSourceProps,
} from "@unpic/core";
import { expect } from "vitest";

export function normalise(value: unknown) {
  if (value === undefined || value === null) {
    return null;
  }
  return value.toString();
}

function expectPropsToMatch(
  expected: CoreImageAttributes<unknown> | CoreSourceAttributes,
  element: HTMLElement,
) {
  for (const [key, value] of Object.entries(expected)) {
    if (key === "style") {
      for (const [styleKey, styleValue] of Object.entries(
        value as Record<string, string>,
      )) {
        if (styleValue) {
          expect(normalise(element.style.getPropertyValue(styleKey))).toBe(
            normalise(styleValue),
          );
        }
      }
    } else {
      expect(
        normalise(element.getAttribute(key)),
        `${key} does not match`,
      ).toBe(normalise(value));
    }
  }
}

/**
 * Given an HTMLImageElement and a set of component props, assert that the generated
 * element matches the props generated by transformProps, when passed those props.
 * @param image The image element rendered by the component
 * @param providedProps The props passed to the component
 */
export function expectImagePropsToMatchTransformed(
  image: HTMLImageElement,
  providedProps: UnpicImageProps<
    CoreImageAttributes<CSSStyleDeclaration>,
    CSSStyleDeclaration
  >,
) {
  const expected = transformProps<
    CoreImageAttributes<CSSStyleDeclaration>,
    CSSStyleDeclaration
  >(providedProps);

  expectPropsToMatch(expected, image);
}

/**
 * Given an HTMLSourceElement and a set of component props, assert that the generated
 * element matches the props generated by transformProps, when passed those props.
 * @param source The source element rendered by the component
 * @param providedProps The props passed to the component
 */
export function expectSourcePropsToMatchTransformed(
  source: HTMLSourceElement,
  providedProps: UnpicSourceProps,
) {
  const expected = transformSourceProps(providedProps);

  expectPropsToMatch(expected, source);
}

